    public function register(Request $request)
    {
        $this->validate($request, [
            'name'     =>'required',
            'email'    => 'required|email',
            'password' =>'required|min:8'
        ]);

        $data = User::create([
            'name' => $request['name'],
            'email' => $request['email'],
            'password' => Hash::make($request['password'])
        ]);
        $data->save();
        return response()->json([
            'message'=>'user created successfull.',
            'status'=> 200
        ]);
    }

